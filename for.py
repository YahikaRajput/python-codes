# for syntax
# for variable in sequence:
#    # code block to be executed
# examples of for loops
# ex-1
for i in range(1,11):
    print(i)

# ex-2
for i in range(1,20,2):
    print(i) 

# ex-3
for i in range(20,1,-1):
    print(i)

# ex-4
for i in range(1,1,1):
    print(i)  
     